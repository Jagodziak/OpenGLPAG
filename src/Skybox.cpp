Skybox::Skybox(std::string cubemapPath)
{
    cubemap.load(cubemapPath);

    // Generate and bind VAO
    glGenVertexArrays(1, &VAO);
    glBindVertexArray(VAO);

    // Generate, bind and fill VBO
    glGenBuffers(1, &VBO);
    glBindBuffer(GL_ARRAY_BUFFER, VBO);
    glBufferData(GL_ARRAY_BUFFER, 108 * sizeof(float), skyboxVertices, GL_STATIC_DRAW);

    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 3 * sizeof(float), (void*)0);
    glEnableVertexAttribArray(0);

    // Bind to null for safety
    glBindVertexArray(0);
}

void Skybox::draw(Shader& shader)
{
    shader.use();
    glActiveTexture(GL_TEXTURE0); // Activate texture unit 0
    glBindTexture(GL_TEXTURE_2D, cubemap.getCubemapId());
    shader.setInt("cubemap", 0);

    glBindVertexArray(VAO);
    glDrawArrays(GL_TRIANGLES, 0, 108 / 3);
}
